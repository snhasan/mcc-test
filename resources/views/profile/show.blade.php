          }
        }

        function showPosition(position) {
          var lat = position.coords.latitude ;
          var lng = position.coords.longitude;

          $.ajax({
                method:'POST',
                url:'{{ route("getLoc")}}',
                dataType: 'json',
                data : {
                    '_token':'{{ csrf_token()}}',
                        lat: lat,
                        lng: lng,
                },
